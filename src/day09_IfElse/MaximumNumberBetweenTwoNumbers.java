package day09_IfElse;

public class MaximumNumberBetweenTwoNumbers {
    public static void main(String[] args) {
        int n1=100,
            n2=90;
        if(n1>n2){
            System.out.println(n1 + " is the maximum number.");
        }else {
            System.out.println(n2 + " is the maximum number.");
        }

    }
}
