package day09_IfElse;

public class MinimumNumberBetweenTwoNumbers {
    public static void main(String[] args) {
        int n1=50,
                n2=90;
        if(n1<n2){
            System.out.println(n1 + " is the minimum number");
        }else {
            System.out.println(n2 + " is the minimum number");
        }
    }
}
