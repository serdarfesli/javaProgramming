package day07_Operators;

public class UnaryOperators {

    public static void main(String[] args) {
        System.out.println("----------------------");
int a= 5;
++a;
        System.out.println(a);
        System.out.println(--a);
        System.out.println("----------------------");

        int z=45;

        System.out.println(++z);
        System.out.println(z++);
        System.out.println(z);

        int q=30;
        System.out.println(--q);
        System.out.println(q--);
        System.out.println(q);

    }
}
