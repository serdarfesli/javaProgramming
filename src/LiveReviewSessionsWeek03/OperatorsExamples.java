package LiveReviewSessionsWeek03;

public class OperatorsExamples {
    public static void main(String[] args) {
        //Assignment Operator

        int x = 10;
        x+=2; //10+2=12
        x-=2;
        System.out.println("x = " + x);
        x*=2;
        System.out.println("x = " + x);
        x/=2;
        System.out.println("x = " + x);





    }
}
