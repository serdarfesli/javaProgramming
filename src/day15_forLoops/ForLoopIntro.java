package day15_forLoops;

public class ForLoopIntro {
    public static void main(String[] args) {


        for(int i=15; i<=45; i++){
            System.out.print( i +" ");
        }
        System.out.println();
        System.out.println("----------------");
        for(int i=100; i>=50; i--){
            System.out.print(i + " ");
        }
        System.out.println();
        System.out.println("----------------");

        for (int i=2; i<55; i+=2){
            System.out.print(i +" ");
        }
        }
    }

